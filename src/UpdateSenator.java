    private void enter_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enter_buttonActionPerformed
        String id = id_textfield.getText().toString();
        boolean notFound = true;
        try {   
            String url = "jdbc:mysql://localhost:3306/bill_info_system?zeroDateTimeBehavior=convertToNull";
            Connection conn = DriverManager.getConnection(url,"root","useruser");
            Statement stmt = conn.createStatement();
            ResultSet rs;
 
            rs = stmt.executeQuery("SELECT * FROM senator;");
            while ( rs.next() ) {
                String senator_id = rs.getString("senator_id");
                String name = rs.getString("name");
                String position = rs.getString("position_in_senate");
                
                if(senator_id.equals(id)){
                    name_textfield.setText(name);
                    position_textfield.setText(position);
                    notFound=false;
                }
            }
            conn.close();
        } catch (Exception e) {
            System.err.println("Got an exception! ");
            System.err.println(e.getMessage());
        }
        //resets text fields
        if(notFound==true){
            new IDnotfound().setVisible(true);
            name_textfield.setText("");
            position_textfield.setText("");
        }
    }//GEN-LAST:event_enter_buttonActionPerformed

